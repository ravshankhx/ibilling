class Contract {
  final String fish;
  final String id;
  final int number;
  final double amount;
  final String status;
  final String dateTime;
  final String address;
  final int its;

  Contract({
    required this.fish,
    required this.id,
    required this.number,
    required this.amount,
    required this.dateTime,
    required this.status,
    required this.its,
    required this.address,
  });
}
